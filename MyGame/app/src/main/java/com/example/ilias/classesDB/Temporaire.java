public class Temporaire {
    private int id;
    private String nom;
    private String taille;
    private String mot_incomplet;
    private String lettre1;
    private String lettre2;
    private String image;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNom() {
        return nom;
    }

    public void setNom(String nom) {
        this.nom = nom;
    }

    public String getTaille() {
        return taille;
    }

    public void setTaille(String taille) {
        this.taille = taille;
    }

    public String getMot_incomplet() {
        return mot_incomplet;
    }

    public void setMot_incomplet(String mot_incomplet) {
        this.mot_incomplet = mot_incomplet;
    }

    public String getLettre1() {
        return lettre1;
    }

    public void setLettre1(String lettre1) {
        this.lettre1 = lettre1;
    }

    public String getLettre2() {
        return lettre2;
    }

    public void setLettre2(String lettre2) {
        this.lettre2 = lettre2;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
